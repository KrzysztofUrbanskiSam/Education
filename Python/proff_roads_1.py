    def __init__(self, cities_num: int):
        self.edges: Dict[int, Dict[int, int]] = {}
        for idx in range(cities_num):
            self.edges[idx] = {}

    def add_edge(self, node_start: int, node_end: int, dist: int):
        self.edges[node_start][node_end] = dist
        self.edges[node_end][node_start] = dist

    def traverse_bfs(self, start_node: int) -> List[int]:
        distance: List[int] = [-1 for _ in range(len(self.edges))]
        distance[start_node] = 0
        visited_edges: Set[Tuple[int, int]] = set()
        visited_cities: Set[int] = set()

        to_traverse: List[Tuple[int, int]] = []
        to_traverse.extend(self.get_edge_to_traverse(start_node))

        while to_traverse:
            edge = to_traverse.pop(0)
            node_current = edge[0]
            node_next = edge[1]
            if node_next in visited_cities:
                continue
            if edge in visited_edges or (node_next, node_current) in visited_edges:
                continue
            visited_edges.add(edge)
            visited_cities.add(node_next)
            dist = self.edges[node_current][node_next] + distance[node_current]

            if distance[node_next] == -1 or distance[node_next] > dist:
                distance[node_next] = dist

            to_traverse.extend(self.get_edge_to_traverse(edge[1]))

        return distance

    def get_edge_to_traverse(self, start_node: int) -> List[Tuple[int, int]]:
        out: List[Tuple[int, int]] = []
        for next_node in self.edges[start_node].keys():
            out.append((start_node, next_node))
        return out

